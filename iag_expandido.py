def limpar_entrada(texto):
    """
    Limpa e padroniza a entrada da metragem.
    Aceita exemplos como:
    120 - 95,5 - 95.5 - 120 m2 - 120 m²
    """
    texto = texto.strip().lower()
    texto = texto.replace("m²", "")
    texto = texto.replace("m2", "")
    texto = texto.replace(",", ".")
    return texto.strip()

def validar_tamanho(tamanho):
    """
    Valida se o tamanho informado faz sentido.
    """
    if tamanho <= 0:
        return "Por favor, informe um valor maior que zero."

    if tamanho < 4 or tamanho > 100000:
        return (
            "Por favor, informe um tamanho mais realista para o imóvel.\n"
            "Exemplo: 10, 120, 250 ou 1000."
        )

    return None

def classificar_padrao_imovel(tipo, subtipo, tamanho):
    """
    Classifica o padrão do imóvel com base no tipo, subtipo e metragem.

    CASA
    - até 149,9 m² -> casa compacta
    - 150 a 299,9 m² -> casa padrão
    - 300 a 599,9 m² -> casa de alto padrão
    - 600 a 999,9 m² -> mansão
    - 1000 m² ou mais -> mansão de luxo

    APARTAMENTO COMUM
    - até 69,9 m² -> apartamento compacto
    - 70 a 149,9 m² -> apartamento padrão
    - 150 a 299,9 m² -> apartamento de alto padrão
    - 300 a 499,9 m² -> apartamento de luxo
    - 500 m² ou mais -> apartamento de luxo extremo

    COBERTURA
    - até 149,9 m² -> cobertura compacta
    - 150 a 299,9 m² -> cobertura de alto padrão
    - 300 a 499,9 m² -> cobertura de luxo
    - 500 m² ou mais -> cobertura de luxo extremo
    """

    if tipo == "casa":
        if tamanho < 150:
            return "casa compacta"
        elif tamanho < 300:
            return "casa padrão"
        elif tamanho < 600:
            return "casa de alto padrão"
        elif tamanho < 1000:
            return "mansão"
        else:
            return "mansão de luxo"

    if tipo == "apartamento":
        if subtipo == "cobertura":
            if tamanho < 150:
                return "cobertura compacta"
            elif tamanho < 300:
                return "cobertura de alto padrão"
            elif tamanho < 500:
                return "cobertura de luxo"
            else:
                return "cobertura de luxo extremo"
        else:
            if tamanho < 70:
                return "apartamento compacto"
            elif tamanho < 150:
                return "apartamento padrão"
            elif tamanho < 300:
                return "apartamento de alto padrão"
            elif tamanho < 500:
                return "apartamento de luxo"
            else:
                return "apartamento de luxo extremo"

    return "imóvel"

