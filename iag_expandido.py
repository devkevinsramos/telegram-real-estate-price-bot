def montar_entrada_modelo(tamanho, tipo, subtipo, zona):
    """
    Monta uma linha de entrada com exatamente as mesmas colunas usadas no treino.
    """

    # Cria um dicionário com todas as colunas zeradas
    entrada = {coluna: 0 for coluna in X.columns}

    # Preenche a metragem
    if "tamanho_m2" in entrada:
        entrada["tamanho_m2"] = tamanho

    # Marca o tipo do imóvel escolhido
    coluna_tipo = f"tipo_imovel_{tipo}"
    if coluna_tipo in entrada:
        entrada[coluna_tipo] = 1

    # Marca o subtipo do apartamento
    coluna_subtipo = f"subtipo_apartamento_{subtipo}"
    if coluna_subtipo in entrada:
        entrada[coluna_subtipo] = 1

    # Marca a zona escolhida
    coluna_zona = f"zona_{zona}"
    if coluna_zona in entrada:
        entrada[coluna_zona] = 1

    # Converte para DataFrame com uma única linha
    return pd.DataFrame([entrada])

def prever_preco(tamanho, tipo, subtipo, zona):
    """
    Realiza a previsão.
    """
    entrada_modelo = montar_entrada_modelo(tamanho, tipo, subtipo, zona)
    preco_previsto = modelo.predict(entrada_modelo)[0]
    return preco_previsto

def montar_resposta_estimativa(tipo, subtipo, zona, tamanho, preco_formatado):
    """
    Monta a resposta final da simulação.
    """
    classificacao = classificar_padrao_imovel(tipo, subtipo, tamanho)
    observacao = faixa_imovel(tipo, subtipo, tamanho)

    return (
        f"Perfeito! Para um(a) {classificacao} de {tamanho:.1f} m², "
        f"localizado(a) em {zona.title()},\n"
        f"o preço estimado é de {preco_formatado}.\n\n"
        f"{observacao}\n\n"
        "Deseja fazer outra simulação?\n"
        "Use /novo para calcular novamente ou /encerrar para finalizar."
    )

