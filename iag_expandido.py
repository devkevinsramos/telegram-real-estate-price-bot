@bot.message_handler(content_types=['text'])
def conversar(mensagem):
    """
    Controla as etapas da conversa:
    1. Tipo do imóvel
    2. Subtipo do apartamento (se necessário)
    3. Zona
    4. Metragem
    """
    chat_id = mensagem.chat.id
    texto_original = mensagem.text.strip()
    texto = texto_original.lower()

    # Se o usuário ainda não tiver um fluxo iniciado, iniciamos automaticamente
    if chat_id not in estado_usuarios:
        iniciar_fluxo(chat_id)

    etapa = estado_usuarios[chat_id]["etapa"]

    # ETAPA 1 - ESCOLHER TIPO DO IMÓVEL
    if etapa == "tipo_imovel":
        if texto in ["1", "casa"]:
            estado_usuarios[chat_id]["tipo"] = "casa"
            estado_usuarios[chat_id]["subtipo"] = "nao_se_aplica"
            estado_usuarios[chat_id]["etapa"] = "zona"

            bot.reply_to(
                mensagem,
                "Ótimo! Agora escolha a zona do imóvel:\n"
                "1 - Zona Sul\n"
                "2 - Zona Norte\n"
                "3 - Zona Oeste\n"
                "4 - Zona Leste\n"
                "5 - Centro"
            )
            return

        elif texto in ["2", "apartamento", "apto"]:
            estado_usuarios[chat_id]["tipo"] = "apartamento"
            estado_usuarios[chat_id]["etapa"] = "subtipo_apartamento"

            bot.reply_to(
                mensagem,
                "Perfeito! Agora escolha o subtipo do apartamento:\n"
                "1 - Comum\n"
                "2 - Cobertura"
            )
            return

        else:
            bot.reply_to(
                mensagem,
                "Por favor, escolha uma opção válida:\n"
                "1 - Casa\n"
                "2 - Apartamento"
            )
            return

    # ETAPA 2 - ESCOLHER SUBTIPO DO APARTAMENTO
    if etapa == "subtipo_apartamento":
        if texto in ["1", "comum", "apartamento comum"]:
            estado_usuarios[chat_id]["subtipo"] = "comum"
            estado_usuarios[chat_id]["etapa"] = "zona"

            bot.reply_to(
                mensagem,
                "Ótimo! Agora escolha a zona do imóvel:\n"
                "1 - Zona Sul\n"
                "2 - Zona Norte\n"
                "3 - Zona Oeste\n"
                "4 - Zona Leste\n"
                "5 - Centro"
            )
            return

        elif texto in ["2", "cobertura"]:
            estado_usuarios[chat_id]["subtipo"] = "cobertura"
            estado_usuarios[chat_id]["etapa"] = "zona"

            bot.reply_to(
                mensagem,
                "Perfeito! Agora escolha a zona do imóvel:\n"
                "1 - Zona Sul\n"
                "2 - Zona Norte\n"
                "3 - Zona Oeste\n"
                "4 - Zona Leste\n"
                "5 - Centro"
            )
            return

        else:
            bot.reply_to(
                mensagem,
                "Por favor, escolha uma opção válida:\n"
                "1 - Comum\n"
                "2 - Cobertura"
            )
            return

    # ETAPA 3 - ESCOLHER ZONA
    if etapa == "zona":
        mapa_zonas = {
            "1": "zona sul",
            "2": "zona norte",
            "3": "zona oeste",
            "4": "zona leste",
            "5": "centro",
            "zona sul": "zona sul",
            "zona norte": "zona norte",
            "zona oeste": "zona oeste",
            "zona leste": "zona leste",
            "centro": "centro",
            "sul": "zona sul",
            "norte": "zona norte",
            "oeste": "zona oeste",
            "leste": "zona leste"
        }

        if texto in mapa_zonas:
            estado_usuarios[chat_id]["zona"] = mapa_zonas[texto]
            estado_usuarios[chat_id]["etapa"] = "metragem"

            bot.reply_to(
                mensagem,
                "Perfeito! Agora envie o tamanho do imóvel em m².\n"
                "Exemplo: 120"
            )
            return

        else:
            bot.reply_to(
                mensagem,
                "Por favor, escolha uma zona válida:\n"
                "1 - Zona Sul\n"
                "2 - Zona Norte\n"
                "3 - Zona Oeste\n"
                "4 - Zona Leste\n"
                "5 - Centro"
            )
            return

    # ETAPA 4 - INFORMAR METRAGEM E CALCULAR
    if etapa == "metragem":
        try:
            # Limpa a entrada para aceitar formatos como 95,5 ou 120 m²
            texto_limpo = limpar_entrada(texto_original)

            # Converte a metragem para número
            tamanho = float(texto_limpo)

            # Valida se a metragem é aceitável
            mensagem_erro = validar_tamanho(tamanho)
            if mensagem_erro:
                bot.reply_to(mensagem, mensagem_erro)
                return

            # Recupera as escolhas anteriores do usuário
            tipo = estado_usuarios[chat_id]["tipo"]
            subtipo = estado_usuarios[chat_id]["subtipo"]
            zona = estado_usuarios[chat_id]["zona"]

            # Faz a previsão usando todas as variáveis
            preco_previsto = prever_preco(tamanho, tipo, subtipo, zona)

            # Formata o valor para o padrão brasileiro
            preco_formatado = formatar_reais(preco_previsto)

            # Monta a resposta final
            resposta = montar_resposta_estimativa(
                tipo,
                subtipo,
                zona,
                tamanho,
                preco_formatado
            )

            # Envia a resposta ao usuário
            bot.reply_to(mensagem, resposta)

        except ValueError:
            bot.reply_to(
                mensagem,
                "Não consegui interpretar sua mensagem.\n"
                "Envie apenas o tamanho do imóvel em m².\n"
                "Exemplos válidos: 80, 95,5 ou 120 m².\n\n"
                "Se precisar, use /ajuda."
            )

