# TRATAMENTO DAS MENSAGENS DE TEXTO
@bot.message_handler(content_types=['text'])
def conversar(mensagem):
    texto = mensagem.text.strip().lower()

    try:
        # Troca vírgula por ponto para aceitar formatos como 95,5
        texto = texto.replace(",", ".")

        # Converte o texto para número
        tamanho = float(texto)

        # Valida se o valor é positivo
        if tamanho <= 0:
            bot.reply_to(
                mensagem,
                "Por favor, informe um valor maior que zero."
            )
            return

        # Validação simples para evitar valores absurdos
        if tamanho < 4 or tamanho > 1000:
            bot.reply_to(
                mensagem,
                "Por favor, informe um tamanho mais realista para o imóvel.\n"
                "Exemplo: 10, 120 ou 250."
            )
            return

        # Faz a previsão
        preco_previsto = prever_preco(tamanho)

        # Formata o valor em reais
        preco_formatado = formatar_reais(preco_previsto)

        # Responde ao usuário
        bot.reply_to(
            mensagem,
            f"Para um imóvel de {tamanho:.1f} m²,\n"
            f"o preço estimado é de {preco_formatado}.\n\n"
            "Deseja fazer outra simulação?\n"
            "Use /novo para calcular novamente ou /encerrar para finalizar."
        )

    except ValueError:
        bot.reply_to(
            mensagem,
            "Não consegui interpretar sua mensagem.\n"
            "Envie apenas o tamanho do imóvel em m².\n"
            "Exemplo: 120.\n\n"
            "Se precisar, use /ajuda."
        )

