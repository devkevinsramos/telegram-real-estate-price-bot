# LEITURA DO CSV E TREINAMENTO DO MODELO

# Lê o arquivo
df = pd.read_csv("dados_imobiliaria.csv")

# X = variável de entrada (tamanho do imóvel)
X = df[["tamanho_m2"]]

# y = variável de saída (preço do imóvel)
y = df["preco_reais"]

# Cria e treina o modelo
modelo = LinearRegression()
modelo.fit(X, y)

# FUNÇÕES

# Formata um número para o padrão brasileiro de moeda.
def formatar_reais(valor):

    valor_formatado = f"{valor:,.2f}"
    valor_formatado = valor_formatado.replace(",", "X")
    valor_formatado = valor_formatado.replace(".", ",")
    valor_formatado = valor_formatado.replace("X", ".")
    return f"R$ {valor_formatado}"

# Recebe o tamanho do imóvel em m² e retorna o preço previsto pelo modelo.
def prever_preco(tamanho):

    nova_casa = pd.DataFrame({
        "tamanho_m2": [tamanho]
    })

    preco_previsto = modelo.predict(nova_casa)[0]
    return preco_previsto

